package AQues9.P1;


public class TwoDim {
    private double x;
    private double y;


    public TwoDim(double x, double y)
    {
        this.x=x;
        this.y=y;
    }


    @Override
    public String toString() {
        return "TwoDim{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}

